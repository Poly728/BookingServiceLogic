package org.example.bookingservicelogic.entity.enums;

/**
 * Enumeration representing types of rooms/accommodations available.
 *
 */
public enum RoomType {

    /** Standard room with basic amenities */
    STANDARD,

    /** Deluxe room with enhanced amenities */
    DELUXE,

    /** Suite with separate living area */
    SUITE,

    /** Penthouse - top floor luxury accommodation */
    PENTHOUSE,

    /** Studio apartment */
    STUDIO,

    /** Full apartment with multiple rooms */
    APARTMENT,

    /** Cabin - typically in mountain/nature locations */
    CABIN,

    /** Entire house rental */
    HOUSE
}
